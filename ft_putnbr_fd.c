#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	t;

	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = n * (-1);
	}
	if (n >= 0 && n <= 9)
	{
		t = n + '0';
		write (fd, &t, 1);
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
